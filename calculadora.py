def suma (a, b):
  return a + b

print (f"la suma de 2 + 3 es: {suma(2, 3)}")

def resta (a, b):
  return a - b

print (f"la resta de 2 - 3 es: {resta(2, 3)}")

def multiplicacion (a, b):
  return a * b

print (f"la multiplicacion de 2 * 3 es: {multiplicacion(2, 3)}")

def division (a, b):
  return a / b

print (f"la division de 2 / 3 es: {division(2, 3)}")
